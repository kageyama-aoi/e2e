Feature('ログイン後の画面遷移');

Scenario('ログイン後に複数のリンクを辿って画面遷移する', ({ I, loginPage }) => {
  // ----------------------------------------------------
  // 1. ログイン処理
  // ----------------------------------------------------
  // 既存のPage Objectを使ってログインします
  loginPage.login(process.env.ADMIN_USER, process.env.ADMIN_PASSWORD);

  // ログインが成功したことを確認します
  loginPage.seeLogout();

  // ----------------------------------------------------
  // 2. 画面遷移 その1 (リンククリック → 表示確認)
  // ----------------------------------------------------
  // 'マイページへ' の部分は、実際のリンクのテキストやセレクタに置き換えてください
  I.click('管理者メニュー');
  // 'マイページ' の部分は、遷移後のページに表示されるユニークなテキストに置き換えてください
  I.see('JSON入力');

  // ----------------------------------------------------
  // 3. 画面遷移 その2 (リンククリック → 表示確認)
  // ----------------------------------------------------
  // 'プロフィール編集' の部分は、実際のリンクのテキストやセレクタに置き換えてください
  I.click('ログイン');
  // 'プロフィールの編集' の部分は、遷移後のページに表示されるユニークなテキストに置き換えてください
  I.see('パーソンカテゴリ');
  I.saveScreenshot('API.png')
});